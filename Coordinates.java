public class Coordinates {

    //Atributos
    private int x;
    private int y;

    //Constructor
    public Coordinates() {
       
    }
    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    //Gettet y setters
    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }


    
}
